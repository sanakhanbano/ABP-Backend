package com.mazars.in.model.intermediatemodel;

public class CategoryMasterData {
	private String Organization_Code;
	private String Vertical_Code;
	private String Category_name;
	private String Category_Description;
	public String getOrganization_Code() {
		return Organization_Code;
	}
	public void setOrganization_Code(String organization_Code) {
		Organization_Code = organization_Code;
	}
	public String getVertical_Code() {
		return Vertical_Code;
	}
	public void setVertical_Code(String vertical_Code) {
		Vertical_Code = vertical_Code;
	}
	public String getCategory_name() {
		return Category_name;
	}
	public void setCategory_name(String category_name) {
		Category_name = category_name;
	}
	public String getCategory_Description() {
		return Category_Description;
	}
	public void setCategory_Description(String category_Description) {
		Category_Description = category_Description;
	}
}
