package com.mazars.in.model.intermediatemodel;

public class VerticalMasterData {
	private String Organization_Code;
	private String Vertical_Code;
	private String Vertical_name;
	private String Vertical_Description;
	public String getOrganization_Code() {
		return Organization_Code;
	}
	public void setOrganization_Code(String organization_Code) {
		Organization_Code = organization_Code;
	}
	public String getVertical_Code() {
		return Vertical_Code;
	}
	public void setVertical_Code(String vertical_Code) {
		Vertical_Code = vertical_Code;
	}
	public String getVertical_name() {
		return Vertical_name;
	}
	public void setVertical_name(String vertical_name) {
		Vertical_name = vertical_name;
	}
	public String getVertical_Description() {
		return Vertical_Description;
	}
	public void setVertical_Description(String vertical_Description) {
		Vertical_Description = vertical_Description;
	}
}
