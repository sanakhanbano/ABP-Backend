package com.mazars.in.model.intermediatemodel;

public class CostCenterMasterData {
	private String Organization_Code;
	private String Cost_Center;
	private String Cost_Center_Description;
	public String getOrganization_Code() {
		return Organization_Code;
	}
	public void setOrganization_Code(String organization_Code) {
		Organization_Code = organization_Code;
	}
	public String getCost_Center() {
		return Cost_Center;
	}
	public void setCost_Center(String cost_Center) {
		Cost_Center = cost_Center;
	}
	public String getCost_Center_Description() {
		return Cost_Center_Description;
	}
	public void setCost_Center_Description(String cost_Center_Description) {
		Cost_Center_Description = cost_Center_Description;
	}
}
